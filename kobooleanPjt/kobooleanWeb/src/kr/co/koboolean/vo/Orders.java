package kr.co.koboolean.vo;

public class Orders {
	private String order_id;
	private String order_num;
	private String order_name;
	private String order_address;
	private int order_table;
	private String order_phone;
	private String order_latis;
	private String order_longs;
	
	
	
	
	public String getOrder_id() {
		return order_id;
	}
	public void setOrder_id(String order_id) {
		this.order_id = order_id;
	}
	public String getOrder_num() {
		return order_num;
	}
	public void setOrder_num(String order_num) {
		this.order_num = order_num;
	}
	public String getOrder_name() {
		return order_name;
	}
	public void setOrder_name(String order_name) {
		this.order_name = order_name;
	}
	public String getOrder_address() {
		return order_address;
	}
	public void setOrder_address(String order_address) {
		this.order_address = order_address;
	}
	public int getOrder_table() {
		return order_table;
	}
	public void setOrder_table(int order_table) {
		this.order_table = order_table;
	}
	public String getOrder_phone() {
		return order_phone;
	}
	public void setOrder_phone(String order_phone) {
		this.order_phone = order_phone;
	}
	public String getOrder_latis() {
		return order_latis;
	}
	public void setOrder_latis(String order_latis) {
		this.order_latis = order_latis;
	}
	public String getOrder_longs() {
		return order_longs;
	}
	public void setOrder_longs(String order_longs) {
		this.order_longs = order_longs;
	}

	
	
	
	
}
