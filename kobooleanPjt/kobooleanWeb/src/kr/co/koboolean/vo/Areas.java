package kr.co.koboolean.vo;

public class Areas {
	private String area_id;
	private String area_name;
	private String area_address;
	private String area_num;
	private String area_intro;

	public String getArea_id() {
		return area_id;
	}

	public void setArea_id(String area_id) {
		this.area_id = area_id;
	}

	public String getArea_name() {
		return area_name;
	}

	public void setArea_name(String area_name) {
		this.area_name = area_name;
	}

	public String getArea_address() {
		return area_address;
	}

	public void setArea_address(String area_address) {
		this.area_address = area_address;
	}

	public String getArea_num() {
		return area_num;
	}

	public void setArea_num(String area_num) {
		this.area_num = area_num;
	}

	public String getArea_intro() {
		return area_intro;
	}

	public void setArea_intro(String area_intro) {
		this.area_intro = area_intro;
	}

}
